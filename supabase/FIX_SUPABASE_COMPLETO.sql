create or replace function public.vl_registrar_carregamento_captacao()
returns trigger as $$
begin
  if new.status = 'carregou' then
    insert into public.vl_motorista_carregamentos (
      motorista_id, captacao_id, filial_id, operacao, captador_usuario, observacao, carregou_em
    ) values (
      new.motorista_id, new.id, new.filial_id, new.operacao, new.captador_usuario, new.observacao, now()
    )
    on conflict (captacao_id) do update set
      motorista_id = excluded.motorista_id,
      filial_id = excluded.filial_id,
      operacao = excluded.operacao,
      captador_usuario = excluded.captador_usuario,
      observacao = excluded.observacao;
  end if;

  if tg_op = 'UPDATE' and old.status is distinct from new.status then
    insert into public.vl_captacao_eventos (captacao_id, status_anterior, status_novo, usuario, observacao)
    values (new.id, old.status, new.status, new.captador_usuario, new.observacao);
  elsif tg_op = 'INSERT' then
    insert into public.vl_captacao_eventos (captacao_id, status_anterior, status_novo, usuario, observacao)
    values (new.id, null, new.status, new.captador_usuario, new.observacao);
  end if;

  return new;
end;
$$ language plpgsql;

drop trigger if exists trg_vl_captacoes_carregamento on public.vl_captacoes;
create trigger trg_vl_captacoes_carregamento after insert or update on public.vl_captacoes
for each row execute function public.vl_registrar_carregamento_captacao();

