create or replace view public.vw_motoristas_que_carregam as
select
  m.id,
  m.nome,
  m.telefone,
  m.telefone_limpo,
  count(mc.id)::integer as carregamentos,
  array_remove(array_agg(distinct mc.operacao), null) as operacoes,
  array_remove(array_agg(distinct mc.filial_id), null) as filiais,
  array_remove(array_agg(distinct mc.captador_usuario), null) as captadores,
  max(mc.carregou_em) as ultimo_carregamento,
  max(mc.observacao) as ultima_observacao
from public.vl_motoristas m
join public.vl_motorista_carregamentos mc on mc.motorista_id = m.id
group by m.id, m.nome, m.telefone, m.telefone_limpo;

